public class Concatination {
    public static void main(String[] args) {
        int a=19;
        int b=70;
        String name="Aashiya";
        System.out.println(a+b+name);//ans 89Aashiya
        System.out.println(b+a+name);//ans 89Aashiay
        System.out.println(name+a+b);//ans Aashiya1970
    }
}
